# Exercício 1 — Repetição básica

# Utilize um while para imprimir os números de 1 a 10, um por linha.

# Exercício 2 — Contagem regressiva

# Utilize um while para imprimir os números de 10 a 1, em ordem decrescente.

# Exercício 3 — Soma até parada

# Peça números inteiros ao usuário e utilize um while para somar os valores até que o usuário digite 0.

# Exercício 4 — Validação de entrada

# Solicite um número maior que zero.
# Enquanto o valor informado for inválido, continue pedindo um novo valor.

# Exercício 5 — Contador com condição

# Utilize um while para contar quantos números pares o usuário digitar, encerrando quando ele informar -1.

# Exercício 6 — Menu repetitivo

# Crie um menu de opções que se repita com while até o usuário escolher sair.
# Cada opção deve executar uma ação diferente.

# Exercício 7 — Média de valores

# Solicite valores ao usuário até que ele informe um valor de parada.
# Utilize while para calcular e exibir a média dos valores válidos.

# Exercício 8 — Controle de tentativas

# Implemente um sistema de senha onde o usuário tem no máximo 3 tentativas para acertar a senha correta.

# Exercício 9 — Simulação de caixa eletrônico

# Utilize while para simular saques sucessivos de uma conta até que o saldo seja insuficiente ou o usuário decida sair.

# Exercício 10 — Processamento contínuo de dados

# Crie um programa que leia dados de várias pessoas (idade, renda e histórico).
# Utilize while para:

# validar entradas,

# processar cada registro,

# gerar um resumo final com estatísticas.