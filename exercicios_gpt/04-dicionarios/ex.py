# Exercício 1 — Criação de dicionário

# Crie um dicionário para armazenar nome e idade de uma pessoa e exiba o dicionário completo.

# Exercício 2 — Acesso por chave

# Dado um dicionário com informações de um produto (nome, preço, quantidade), exiba apenas o preço do produto.

# Exercício 3 — Inserção de novos pares

# Adicione uma nova chave (por exemplo, categoria) a um dicionário já existente e exiba o resultado.

# Exercício 4 — Atualização de valores

# Atualize o valor de uma chave existente em um dicionário (por exemplo, alterar o preço de um produto).

# Exercício 5 — Percorrendo dicionários

# Utilize um laço para percorrer um dicionário e exibir todas as chaves e valores.

# Exercício 6 — Contagem com dicionário

# Leia uma frase do usuário e utilize um dicionário para contar quantas vezes cada palavra aparece.

# Exercício 7 — Verificação de existência

# Verifique se uma determinada chave existe em um dicionário antes de tentar acessá-la.

# Exercício 8 — Lista de dicionários

# Crie uma lista contendo vários dicionários (por exemplo, cadastro de pessoas).
# Utilize laços para exibir os dados de cada registro.

# Exercício 9 — Agrupamento de dados

# Utilize um dicionário para agrupar valores por categoria (ex.: alunos por turma, produtos por tipo).

