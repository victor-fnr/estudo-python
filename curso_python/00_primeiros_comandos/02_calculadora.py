#CALCULOS MATEMÁTICOS

#SOMA
print(10 + 10)
print('10 + 10 = ', 10 + 10)

#SUBTRAÇÃO
print(10 - 5)
print('10 - 5 = ', 10 - 5)

#MULTIPLICAÇÃO
print(10 * 10)
print('10 x 10 = ', 10 * 10)
print(9 * 14)

#DIVISÃO
print('10 / 10 = ', 10 / 10)
print('10 / 3 = ', 10 / 3)

#DIVISÃO COM INTEIRA PARTE
print('10 // 3 = ', 10 // 3)

#RESTO DA DIVISÃO
print('O resto de 10 / 3 = ', 10 % 3)

#POTÊNCIA
print('3 ** 4 = ', 3 ** 4)